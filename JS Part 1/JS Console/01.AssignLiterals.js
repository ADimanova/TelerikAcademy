function printLiterals() {
    jsConsole.writeLine('Integer: ' + integer);
    jsConsole.writeLine('Floating-point: ' + float);
    jsConsole.writeLine('String: ' + stringValue);
    jsConsole.writeLine('Boolean: ' + bool);
    jsConsole.writeLine('Undefined: ' + unknown);
    jsConsole.writeLine('Null: ' + noValue);
}